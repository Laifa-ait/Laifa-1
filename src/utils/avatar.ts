// Convert a raw SVG string to a completely safe and responsive data URI
function svgToDataUri(svg: string): string {
  // Replace symbols that might cause issues in simple Data URIs
  const cleanSvg = svg
    .replace(/"/g, "'")
    .replace(/%/g, '%25')
    .replace(/#/g, '%23')
    .replace(/{/g, '%7B')
    .replace(/}/g, '%7D')
    .replace(/</g, '%3C')
    .replace(/>/g, '%3E')
    .replace(/\s+/g, ' '); // collapse whitespace
  return `data:image/svg+xml;utf8,${cleanSvg}`;
}

